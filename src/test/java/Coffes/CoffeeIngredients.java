package Coffes;

public class CoffeeIngredients {
    private String size, type;
    private Boolean cinnamon, skimmedMilk, sugar, splenda, cream;
    private int coffeeTablespoons, iceCubes, numberOfCoffees;
    private float coffeeCups, ouncesWater, ouncesMilk, oucesLicor;

    public CoffeeIngredients() {
    }

    public Boolean getCinnamon() {
        return cinnamon;
    }

    public void setCinnamon(Boolean cinnamon) {
        this.cinnamon = cinnamon;
    }

    public Boolean getSkimmedMilk() {
        return skimmedMilk;
    }

    public void setSkimmedMilk(Boolean skimmedMilk) {
        this.skimmedMilk = skimmedMilk;
    }

    public Boolean getSugar() {
        return sugar;
    }

    public void setSugar(Boolean sugar) {
        this.sugar = sugar;
    }

    public Boolean getSplenda() {
        return splenda;
    }

    public void setSplenda(Boolean splenda) {
        this.splenda = splenda;
    }

    public Boolean getCream() {
        return cream;
    }

    public void setCream(Boolean cream) {
        this.cream = cream;
    }

    public float getCoffeeCups() {
        return coffeeCups;
    }

    public void setCoffeeCups(float coffeCups) {
        this.coffeeCups = coffeCups;
    }

    public int getCoffeeTablespoons() {
        return coffeeTablespoons;
    }

    public void setCoffeeTablespoons(int coffeeTablespoons) {
        this.coffeeTablespoons = coffeeTablespoons;
    }

    public float getOuncesWater() {
        return ouncesWater;
    }

    public void setOuncesWater(float ouncesWater) {
        this.ouncesWater = ouncesWater;
    }

    public float getOuncesMilk() {
        return ouncesMilk;
    }

    public void setOuncesMilk(float ouncesMilk) {
        this.ouncesMilk = ouncesMilk;
    }

    public float getOucesLicor() {
        return oucesLicor;
    }

    public void setOucesLicor(float oucesLicor) {
        this.oucesLicor = oucesLicor;
    }

    public int getIceCubes() {
        return iceCubes;
    }

    public void setIceCubes(int iceCubes) {
        this.iceCubes = iceCubes;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getNumberOfCoffees() {
        return numberOfCoffees;
    }

    public void setNumberOfCoffees(int numberOfCoffees) {
        this.numberOfCoffees = numberOfCoffees;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
